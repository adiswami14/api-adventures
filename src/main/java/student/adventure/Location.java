package student.adventure;

public class Location {
    private int x;
    private int y;

    public Location() {
    }

    /**
     * @return the x-value of the Location
     */
    public int getX() {
        return x;
    }

    /**
     * @return the y-value of the Location
     */
    public int getY() {
        return y;
    }
}
