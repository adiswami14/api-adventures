package student.adventure;

public class Location {
    private int x; //x-coordinate of location
    private int y; //y-coordinate of location

    /**
     * @return the x-value of the Location
     */
    public int getX() {
        return x;
    }

    /**
     * @return the y-value of the Location
     */
    public int getY() {
        return y;
    }
}
